class Break_2 
{
	public static void main(String[] args) 
	{
		int grade = 300;

		switch (grade)
		{
		case 100: 
			System.out.println("grade = 100");
			break;
		case 200:
			System.out.println("grade = 200");
		default:
			System.out.println("default");
			break;
		
		}
		System.out.println("Hello World!");
	}
}
