class For_5 
{
	public static void main(String[] args) 
	{
		
		// For을 이용한 구구단 만들기

		for (int i = 2; i <=9 ;i++ )
		{
			for (int j = 1; j <= 9 ; j++ )
			{
				System.out.println ( i + " * " + j + " = " + (i * j));

			}
			System.out.println();
		}

		System.out.println("Hello World!");
	}
}
