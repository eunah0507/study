class Char_3 
{
	public static void main(String[] args) 
	{

		char single = '\'';
		String dblQuote = "\" Hello \" " ; // 겹따옴표 출력
		String root = "C:\\"; // root 표시

		System.out.println("출력 결과 =>" + single);
		System.out.println("출력 결과 =>" + dblQuote);
		System.out.println("출력 결과 =>" + root);



		System.out.println("Hello World!");
	}
}
