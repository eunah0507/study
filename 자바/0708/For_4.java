class For_4 
{
	public static void main(String[] args) 
	{
		System.out.println("\t 자 이제 카운트 다운을 시작합니다.");
		
		int kk;

		for (kk = 10; kk>= 1;kk-- )
		{
		

		for (int i = 1 ; i < 1000000 ; i++ )
		{
			for (int j = 1; j < 3100 ; j++ )
			{
				for (int k = 1 ; k < 50 ; k++ )
				{
					; //공문
				}
			}
		}
		
		System.out.println("\t\t\t" + kk);
	
		}
		System.out.println("\n \t\t GAME OVER!!!");
		System.out.println("Hello World!");
	}
}
